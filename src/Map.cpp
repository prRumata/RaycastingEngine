sf::Color Map::getColor(size_t x, size_t y) const
{
	switch (m_map[x][y])
	{
	// Тёмный
	case 0:
		return sf::Color(0x282828FF);
	case 1:
	// Бежевый
		return sf::Color(0xa89984FF);
	default:
		return sf::Color(0x000000FF);
	}
}

